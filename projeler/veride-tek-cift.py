#Verilen değere kadar tek çift olan sayıları kontrol eden uygulama Macallantheroot

sayi = int(input("Sayı Giriniz: "))
    
for i in range(1,sayi+1):
    if i % 2 == 0:
        print(str(i) + " Çiftdir." )
else:
    print(str(i) + " Bu Sayı Tekdir.")
    


